#include <Arduino.h>
#include <LD06.h>
#include <array>
#include <algorithm>

LiDAR::LD06 ld06;
static uint8_t s1bufsize[128];

void setup() {
  Serial.begin(115200);
  #if defined(TEENSYDUINO) // バッファーの拡張
  Serial1.addMemoryForRead(s1bufsize, sizeof(s1bufsize));
  #endif
  ld06.init();
}

void loop() {
  std::array<uint8_t,47> data2 = {0x54, 0x2c, 0x15, 0x0e, 0xe2, 0x06, 0x27, 0x01, 0xf2, 0x30, 0x01, 0xf2, 0x34, 0x01, 0xf2, 0x36, 0x01, 0xf1, 0x37, 0x01, 0xf1, 0x38, 0x01, 0xf1, 0x38, 0x01, 0xf1, 0x38, 0x01, 0xf0, 0x3f, 0x01, 0xee, 0x42, 0x01, 0xef, 0x44, 0x01, 0xf0, 0x4b, 0x01, 0xed, 0x64, 0x0a, 0x22, 0x50, 0x84};
  if (ld06.checkCRC(data2)){
    Serial.println("CRC OK");
  } else {
    Serial.println("CRC NG");
  }
  Serial.println(Serial1.available());
  delay(1000);
}